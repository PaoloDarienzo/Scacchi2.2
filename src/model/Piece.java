package model;

/**
 * Enumerate the piece type: the uppercase letters stand for black pieces, the undercase letters stand for white pieces.
 * @author Paolo D'Arienzo VR376656, Serena Cavaletti VR364691
 */
public enum Piece {
	
	/**
	 * Black king
	 */
	K,
	/**
	 * Black queen
	 */
	Q,
	/**
	 * Black rook
	 */
	T,
	/**
	 * Black knight
	 */
	C,
	/**
	 * Black bishop
	 */
	A,
	/**
	 * Black pawn
	 */
	P,
	/**
	 * White king
	 */
	k,
	/**
	 * White queen
	 */
	q,
	/**
	 * White rook
	 */
	t,
	/**
	 * White knight
	 */
	c,
	/**
	 * White bishop
	 */
	a,
	/**
	 * White pawn
	 */
	p,
	/**
	 * Void piece
	 */
	V

}